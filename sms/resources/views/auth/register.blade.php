@push('scripts')
    <script>
        $(document).ready(function() {
            var isEmty = true;
            $('#reg_form').keyup(function() {
                if ($('#name').val() && $('#email').val() && $('#password').val() && $('#password-confirm')
                    .val()) {
                    isEmty = false;
                } else {
                    isEmty = true;
                }
            });
            var i = 0;
            $('#reg_btn').mouseover(function() {
                var p = 0;
                if (i == 0 && isEmty) {
                    p = 100;
                    i = 1
                } else {
                    p = 0;
                    i = 0
                }
                $(this).css("margin-left", p + "px");
            });
        })
    </script>
